class Chip8
{
public:
	unsigned short opcode;//current opcode 2 bytes
	unsigned char memory[4096];//chip8 has total 4K mem
	unsigned char V[16];//16 8-bit registers
	unsigned short I; //index register
	unsigned short pc; //program counter
	/*Memory Map
	0x000-0x1FF - Chip 8 interpreter (contains font set in emu)
	0x050-0x0A0 - Used for the built in 4x5 pixel font set (0-F)
	0x200-0xFFF - Program ROM and work RAM
	*/
	unsigned char gfx[64 * 32];//pixel map(1 or 0)
	unsigned char delay_timer;
	unsigned char sound_timer;
	unsigned short stack[16];//32 byte stack(16 levels)
	unsigned short sp;//stack pointer 
	unsigned char key[16];//HEX keypad(0x0-0xF)
	unsigned char chip8_fontset[80]=
	{
		0xF0, 0x90, 0x90, 0x90, 0xF0, // 0
		0x20, 0x60, 0x20, 0x20, 0x70, // 1
		0xF0, 0x10, 0xF0, 0x80, 0xF0, // 2
		0xF0, 0x10, 0xF0, 0x10, 0xF0, // 3
		0x90, 0x90, 0xF0, 0x10, 0x10, // 4
		0xF0, 0x80, 0xF0, 0x10, 0xF0, // 5
		0xF0, 0x80, 0xF0, 0x90, 0xF0, // 6
		0xF0, 0x10, 0x20, 0x40, 0x40, // 7
		0xF0, 0x90, 0xF0, 0x90, 0xF0, // 8
		0xF0, 0x90, 0xF0, 0x10, 0xF0, // 9
		0xF0, 0x90, 0xF0, 0x90, 0x90, // A
		0xE0, 0x90, 0xE0, 0x90, 0xE0, // B
		0xF0, 0x80, 0x80, 0x80, 0xF0, // C
		0xE0, 0x90, 0x90, 0x90, 0xE0, // D
		0xF0, 0x80, 0xF0, 0x80, 0xF0, // E
		0xF0, 0x80, 0xF0, 0x80, 0x80  // F
	};
	bool drawFlag;
	void initialize();
	void emulateCycle();
	void loadGame(char * ROM);
	void getKey();
	void setKeys();
};
